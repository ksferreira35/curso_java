// Programa que randomiza um número entre 0 até 100
public class ex019 {
    public static void main(String[] args) {

        double ale = Math.random();
        int n = (int) (0 + ale * (100-5)); // Número aleatório de 0 até 100.
        System.out.println(n);
    }
}
