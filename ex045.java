// Programa que mostra um numero sequencial e uma palavra na frente chamada Cambalhota, utilizando uma estrutura de repetição do e o while.
public class ex045 {
    public static void main(String[] args) {
        int cc = 0 ;
        
        do {
            System.out.println("Cambalhota " + cc);
            cc++;

        } while (cc < 4 );
    }
}
