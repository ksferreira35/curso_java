// Programa que facilita a compreensão de Operadores de Atribuição
public class ex016 {
    public static void main(String[] args) {
        int x = 4;
        int y = 4;
        x += 2; // x = x + 2
        y *= 2;
        System.out.println(x);
        System.out.println(y);
    }
}
