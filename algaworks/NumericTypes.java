public class NumericTypes {
    @SuppressWarnings("unused")
    public static void main(String[] args) {
        Byte numberByte = 1; // -128 to 127

        Short numberShort = 1; // -32768 to 32767

        Integer numberInteger = 1; // -2147483648 to 2147483647

        Long numberLong = 1L; // -9.223372037×10¹⁸ to 9.223372037×10¹⁸

    }
}
