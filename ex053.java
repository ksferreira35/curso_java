// Programa que permite descobrir em qual posição cada número está.
public class ex053 {
    public static void main(String[] args) {
        int n[] = {3,4,5,3,1,3,4};
        
        for (int i = 0; i < n.length; i++) {
            System.out.println("Na posição " + i + " temos o valor " + n[i]);
        }
    }
}
